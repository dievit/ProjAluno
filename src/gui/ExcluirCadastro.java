    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
            String cpf = jTextField2.getText();
 
        try {
            String sql = "DELETE FROM weight_evolution WHERE client_id = ?";
            String sql2 = "DELETE FROM users WHERE cpf = ?";
            try (PreparedStatement statement = connection.prepareStatement(sql);
                 PreparedStatement statement2 = connection.prepareStatement(sql2)) {
                statement.setString(1, cpf);
                statement2.setString(1, cpf);
                statement.execute();
                int linhasAfetadas = statement2.executeUpdate();
 
                if (linhasAfetadas > 0) {
                    JOptionPane.showMessageDialog(null, "Histórico completo excluído com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(null, "Nenhum histórico foi excluído. Verifique o CPF.");
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    
    

    }//GEN-LAST:event_jButton3ActionPerformed

